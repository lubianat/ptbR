#'
#' cos
#'
#' Calcula o cosseno.
#' @param x Vetores numéricos ou complexos
#' @export
#' @examples
#' cos(0)
#'
cos <- function(x) {
  cos(x)
}


arccos <- function(x) {
  arccos(x)
}

